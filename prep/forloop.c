#include <stdio.h>


int main(){

   // for loop = repeats a section of code a limited amount of times
   
   /*Inside brackets: (1) integer 'index' (the value to start from) which is usuallyshortened to 'i'
                      (2) condition (if i is les than or equal to 10)
		      (3) increment (increase by 1 each time*/

   for(int i = 1; i <= 10; i++){

      printf("%d\n", i);
   }

   return 0;

}
